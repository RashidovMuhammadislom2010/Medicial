function getQueryParam(param) {
  const urlParams = new URLSearchParams(window.location.search);
  return urlParams.get(param);
}

function renderService(id) {
  const service = services.find((s) => s.id === Number(id));
  if (!service) {
    document.body.innerHTML = "<h2>Xizmat topilmadi</h2>";
    return;
  }

  document.getElementById("service-name").textContent = service.name;
  document.getElementById("service-img").src = service.img;
  document.getElementById("service-img").alt = service.name;
  document.getElementById("service-doctor").textContent = service.doctor;
  document.getElementById("service-price").textContent =
    service.price.toLocaleString();

  const aboutContainer = document.getElementById("service-about");
  aboutContainer.innerHTML = "";
  for (const [key, value] of Object.entries(service.about)) {
    const sectionTitle = document.createElement("h4");
    sectionTitle.textContent = key;
    const sectionText = document.createElement("p");
    sectionText.textContent = value;
    aboutContainer.appendChild(sectionTitle);
    aboutContainer.appendChild(sectionText);
  }
}

const serviceId = getQueryParam("id");
renderService(serviceId);
